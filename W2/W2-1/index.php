<h2>Как нарисовать фигуру</h2>
<p>Чтобы нарисовать фигуру, вам нужно ввести целое число, которое закодирует следующие параметры:</p>
<ul>
    <li><b>Форма (0-3 биты):</b>
        <ul>
            <li>0 - Круг</li>
            <li>1 - Квадрат</li>
            <li>2 - Треугольник</li>
            <li>3 - Прямоугольник</li>
        </ul>
    </li>
    <li><b>Цвет (4-6 биты):</b>
        <ul>
            <li>0 - Красный</li>
            <li>1 - Зеленый</li>
            <li>2 - Синий</li>
            <li>3 - Желтый</li>
            <li>4 - Черный</li>
        </ul>
    </li>
    <li><b>Ширина (7-15 биты):</b> Максимум 512 пикселей</li>
    <li><b>Высота (16-24 биты):</b> Максимум 512 пикселей</li>
</ul>
<p>Например, чтобы нарисовать красный круг размером 100x100 пикселей, вы можете использовать код <b>0b00000000000000000000000000000000</b>
    (в десятичном виде это 0).</p>
<p>Для рисования зеленого квадрата размером 200x200 пикселей, код будет <b>0b00000000000000000000000011001000</b> (в
    десятичном виде это 200).</p>
